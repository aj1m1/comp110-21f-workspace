Number_one: int = int(input("Left-hand side: "))
Number_two: int = int(input("Right-hand side: "))
print(str(Number_one) + " ** " + str(Number_two) + " is " + str(Number_one ** Number_two))
print(str(Number_one) + " / " + str(Number_two) + " is " + str(Number_one / Number_two))
print(str(Number_one) + " // " + str(Number_two) + " is " + str(Number_one // Number_two))
print(str(Number_one) + " % " + str(Number_two) + " is " + str(Number_one % Number_two))