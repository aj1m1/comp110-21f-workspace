First_number: int = int(input("Left-hand side: "))
second_number: int = int(input("Right-hand side: "))
print(str(First_number) + " < " + str(second_number) + " is " + str(First_number < second_number))
print(str(First_number) + " >= " + str(second_number) + " is " + str(First_number >= second_number))
print(str(First_number) + " == " + str(second_number) + " is " + str(First_number == second_number)) 
print(str(First_number) + " != " + str(second_number) + " is " + str(First_number != second_number)) 